/*CLASSES, OBJETOS E CONSTRUTOR: A classe é o "molde", o objeto é a instância criada a partir desse molde, e o construtor é um método usado para cirar e inicializar objetos a partir de uma classe. O "this" serve para referenciar o próprio objeto atual */


/*ENCAPSULAMENTO: serve para a proteção dos dados internos, sendo gerenciados pelos modificadores de acesso.
        PUBLIC - Permite que seja acessado de qualquer lugar
        PRIVATE - Só pode ser acessado dentro da própria classe onde foi criado
        PROTECTED - pode ser acessado dentro da própria classe e por suas classes filhas, mas impede o acesso externo direto*/

/*HERENÇA: permite que uma "classe filha" herde propriedades e métodos de uma "classe mãe" através da palavra-chave extends */

/*POLIMORFISMO:  Objetos diferentes podem ser tratados da mesma forma, mas cada um pode se comportar de maneira diferente.*/

/*ABTRAÇÃO:  Definir o comportamento esperado de algo, escondendo os detalhes de como ele é executado.
(eu sei O QUE faz. Não preciso saber COMO faz)*/